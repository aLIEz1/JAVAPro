package Paper;

public class Paper {
    private String name;
    private float socre;

    void setName(String Name) {
        name = Name;
    }

    void setSocre(float Score) {
        socre = Score;
    }

    public float getSocre() {
        return this.socre;
    }

    public String getName() {
        return this.name;
    }
}
